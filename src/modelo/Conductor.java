package modelo;

public class Conductor {

    
    private int id_cond;
    
    private String nombres_cond;

    private String apellios_cond;
    
    private String f_naci_cond;

    public int getId_cond() {
        return id_cond;
    }

    public void setId_cond(int id_cond) {
        this.id_cond = id_cond;
    }

    public String getNombres_cond() {
        return nombres_cond;
    }

    public void setNombres_cond(String nombres_cond) {
        this.nombres_cond = nombres_cond;
    }

    public String getApellios_cond() {
        return apellios_cond;
    }

    public void setApellios_cond(String apellios_cond) {
        this.apellios_cond = apellios_cond;
    }

    public String getF_naci_cond() {
        return f_naci_cond;
    }

    public void setF_naci_cond(String f_naci_cond) {
        this.f_naci_cond = f_naci_cond;
    }
    
}
