package modelo;


public class Cliente {
    
    private int id_cliente;
    
    private String nombres_cliente;
    
    private String apellidos_cliente;
    
    private String f_naci_cliente;
    
    private String correo_cliente;
    
    private String contrasena;
    
    private String C_contrasena;

    public int getId_cliente() {
        return id_cliente;
    }

    public void setId_cliente(int id_cliente) {
        this.id_cliente = id_cliente;
    }

    public String getNombres_cliente() {
        return nombres_cliente;
    }

    public void setNombres_cliente(String nombres_cliente) {
        this.nombres_cliente = nombres_cliente;
    }

    public String getApellidos_cliente() {
        return apellidos_cliente;
    }

    public void setApellidos_cliente(String apellidos_cliente) {
        this.apellidos_cliente = apellidos_cliente;
    }

    public String getF_naci_cliente() {
        return f_naci_cliente;
    }

    public void setF_naci_cliente(String f_naci_cliente) {
        this.f_naci_cliente = f_naci_cliente;
    }

    public String getCorreo_cliente() {
        return correo_cliente;
    }

    public void setCorreo_cliente(String correo_cliente) {
        this.correo_cliente = correo_cliente;
    }

    public String getContrasena() {
        return contrasena;
    }

    public void setContrasena(String contrasena) {
        this.contrasena = contrasena;
    }

    public String getC_contrasena() {
        return C_contrasena;
    }

    public void setC_contrasena(String C_contrasena) {
        this.C_contrasena = C_contrasena;
    }
    
    
    
}
